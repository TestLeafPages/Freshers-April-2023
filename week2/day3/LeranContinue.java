package week2.day3;

public class LeranContinue {
public static void main(String[] args) {
	for (int i = 1; i <=5; i++) {
		
		if(i==3) {
			System.out.println("Three");
			continue;// to stop the current iteration and enable to execute next iteration
		}
		System.out.println(i);}
	
}
}
